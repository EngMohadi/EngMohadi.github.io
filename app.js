const expertiseGrid = document.querySelector("#expertiseGrid");
const projectsGrid = document.querySelector("#projectsGrid");
const caseGrid = document.querySelector("#caseGrid");
const servicesGrid = document.querySelector("#servicesGrid");
const galleryGrid = document.querySelector("#galleryGrid");
const contactForm = document.querySelector("#contactForm");
const emailLinks = document.querySelectorAll("[data-email-link]");
const whatsappLinks = document.querySelectorAll(".whatsapp-contact-link");
const placeholderNodes = document.querySelectorAll("[data-i18n-placeholder]");

/*
  The email address is built from separated parts so it is not displayed in the
  visible interface and is not stored as one plain text string in the HTML.
  This is not perfect protection: static websites cannot fully hide destination
  emails from advanced scrapers. For true email privacy, use a backend or a
  dedicated contact-form service.
*/
const emailUser = "engmrhadi";
const emailDomain = "gmail.com";
const phoneCountry = "972";
const phoneRest = "567358530";

function getOwnerEmail() {
  return `${emailUser}@${emailDomain}`;
}

function getWhatsAppUrl() {
  return `https://wa.me/${phoneCountry}${phoneRest}`;
}

function setText(data) {
  i18nNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (data[key]) node.textContent = data[key];
  });
  placeholderNodes.forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (data[key]) node.setAttribute("placeholder", data[key]);
  });
}

function renderExpertise(items) {
  expertiseGrid.innerHTML = items
    .map(
      (item) => `
        <article class="expertise-card">
          <h3>${item.title}</h3>
          <p>${item.text}</p>
        </article>
      `
    )
    .join("");
}

function renderProjects(items) {
  projectsGrid.innerHTML = items
    .map(
      (item) => `
        <article class="project-card">
          <img src="${item.image}" alt="${item.title}" loading="lazy" decoding="async" />
          <div class="project-body">
            <h3>${item.title}</h3>
            <p>${item.text}</p>
            ${item.demoNote ? `<p class="demo-note">${item.demoNote}</p>` : ""}
            <div class="project-tags">
              ${item.tags.map((tag) => `<span>${tag}</span>`).join("")}
            </div>
          </div>
        </article>
      `
    )
    .join("");
}

function renderCases(items, labels) {
  caseGrid.innerHTML = items
    .map(
      (item) => `
        <article class="case-card">
          <h3>${item.title}</h3>
          <dl>
            <div>
              <dt>${labels.problem}</dt>
              <dd>${item.problem}</dd>
            </div>
            <div>
              <dt>${labels.solution}</dt>
              <dd>${item.solution}</dd>
            </div>
            <div>
              <dt>${labels.tools}</dt>
              <dd>${item.tools}</dd>
            </div>
            <div>
              <dt>${labels.impact}</dt>
              <dd>${item.impact}</dd>
            </div>
          </dl>
        </article>
      `
    )
    .join("");
}

function renderServices(items) {
  servicesGrid.innerHTML = items
    .map(
      (item) => `
        <article class="service-card">
          <h3>${item.title}</h3>
          <p>${item.text}</p>
        </article>
      `
    )
    .join("");
}

function renderGallery(items) {
  galleryGrid.innerHTML = items
    .map(
      (item) => `
        <figure class="gallery-item">
          <img src="${item.image}" alt="${item.title}" loading="lazy" decoding="async" />
          <figcaption>${item.title}</figcaption>
        </figure>
      `
    )
    .join("");
}

function applyLanguage(lang) {
  const data = content[lang];
  currentLang = lang;
  localStorage.setItem("portfolioLanguage", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = data.dir;
  document.body.setAttribute("dir", data.dir);
  languageToggle.textContent = data.langButton;
  setText(data);
  renderExpertise(data.expertise);
  renderProjects(data.projects);
  renderCases(data.cases, data.labels);
  renderServices(data.services);
  renderGallery(data.gallery);
  updateContactLinks(data);
}

languageToggle.addEventListener("click", () => {
  applyLanguage(currentLang === "ar" ? "en" : "ar");
});

function updateContactLinks(data) {
  const email = getOwnerEmail();
  const mailto = `mailto:${email}?subject=${encodeURIComponent("Portfolio Contact")}`;
  emailLinks.forEach((link) => {
    link.href = mailto;
    link.setAttribute("aria-label", data.emailButton);
  });
  whatsappLinks.forEach((link) => {
    link.href = getWhatsAppUrl();
    link.target = "_blank";
    link.rel = "noopener noreferrer";
    link.setAttribute("aria-label", data.whatsappButton);
  });
}

function buildMessageBody() {
  const name = document.querySelector("#contactName")?.value.trim() || "Not provided";
  const senderEmail = document.querySelector("#contactSenderEmail")?.value.trim() || "Not provided";
  const message = document.querySelector("#contactMessage")?.value.trim() || "Not provided";
  return [`Name: ${name}`, `Sender email: ${senderEmail}`, "", message].join("\n");
}

contactForm?.addEventListener("submit", (event) => {
  event.preventDefault();
  const email = getOwnerEmail();
  const subject = document.querySelector("#contactSubject")?.value.trim() || "Portfolio Contact";
  const body = buildMessageBody();
  window.location.href = `mailto:${email}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
});

applyLanguage(currentLang);
